        public string GuardarPrestamos(int nOpcion, E_Prestamos oCl)
        {
            string Rpta = "";
            SqlConnection SqlCon = new SqlConnection();
            try
            {
                SqlCon = Conexion.getInstancia().CrearConexion();
                SqlCommand Comando = new SqlCommand("USP_GuardarPrestamo", SqlCon);
                Comando.CommandType = CommandType.StoredProcedure;
                Comando.Parameters.Add("@nOpcion",             SqlDbType.Int).Value = nOpcion;
                Comando.Parameters.Add("@cID_PRESTAMO",        SqlDbType.Int).Value = oCl.ID_PRESTAMO; 
                Comando.Parameters.Add("@cID_CLIENTE",         SqlDbType.Int).Value = oCl.ID_CLIENTE;
                Comando.Parameters.Add("@cID_CUENTA",          SqlDbType.Int).Value = oCl.ID_CUENTA;
                Comando.Parameters.Add("@cID_TARJETA_CREDITO", SqlDbType.Int).Value = oCl.ID_TARJETA_CREDITO;
                Comando.Parameters.Add("@cID_TP_PRESTAMO",     SqlDbType.Int).Value = oCl.ID_TP_PRESTAMO;
                Comando.Parameters.Add("@cID_DIM_TP_PAGO",     SqlDbType.Int).Value = oCl.ID_DIM_TP_PAGO;
                Comando.Parameters.Add("@cMONTO_PRESTADO",     SqlDbType.Decimal).Value = oCl.MONTO_PRESTADO;

                SqlCon.Open();
                Rpta = Comando.ExecuteNonQuery() >= 1 ? "OK" : "No se pudieron registrar los datos";
            }
            catch (Exception ex)
            {
                Rpta = ex.Message;
            }
            finally
            {
                if (SqlCon.State == ConnectionState.Open) SqlCon.Close();
            }
            return Rpta;
        }

