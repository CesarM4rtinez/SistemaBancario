        public string GuardarEmpleado(int nOpcion, E_Empleado oCl)
        {
            string Rpta = "";
            SqlConnection SqlCon = new SqlConnection();
            try
            {
                SqlCon = Conexion.getInstancia().CrearConexion();
                SqlCommand Comando = new SqlCommand("USP_GuardarEmpleado", SqlCon);
                Comando.CommandType = CommandType.StoredProcedure;
                Comando.Parameters.Add("@nOpcion",             SqlDbType.Int).Value = nOpcion;
                Comando.Parameters.Add("@cID_EM",              SqlDbType.Int).Value = oCl.ID_EM;
                Comando.Parameters.Add("@cID_CARGO_EM",        SqlDbType.Int).Value = oCl.ID_CARGO_EM;
                Comando.Parameters.Add("@cID_USER",            SqlDbType.Int).Value = oCl.ID_USER;
                Comando.Parameters.Add("@cID_SUCURSAL",        SqlDbType.Int).Value = oCl.ID_SUCURSAL;
                Comando.Parameters.Add("@cNOM_EMPLEADO",       SqlDbType.VarChar).Value = oCl.NOM_EMPLEADO;
                Comando.Parameters.Add("@cAPE_PATE",           SqlDbType.VarChar).Value = oCl.APE_PATE;
                Comando.Parameters.Add("@cAPE_MATE",           SqlDbType.VarChar).Value = oCl.APE_MATE;
                Comando.Parameters.Add("@cDIRECCION",          SqlDbType.VarChar).Value = oCl.DIRECCION;
                Comando.Parameters.Add("@cDNI_EM",             SqlDbType.VarChar).Value = oCl.DNI_EM;
                Comando.Parameters.Add("@cSUELDO",             SqlDbType.Decimal).Value = oCl.SUELDO;

                SqlCon.Open();
                Rpta = Comando.ExecuteNonQuery() >= 1 ? "OK" : "No se pudieron registrar los datos";
            }
            catch (Exception ex)
            {
                Rpta = ex.Message;
            }
            finally
            {
                if (SqlCon.State == ConnectionState.Open) SqlCon.Close();
            }
            return Rpta;
        }

